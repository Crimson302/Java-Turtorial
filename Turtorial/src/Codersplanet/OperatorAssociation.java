package Codersplanet;

public class OperatorAssociation {
    public static void main(String[] args) {
        // Precedence & associativity
        int a = 8*9-64/2;
        /*
        = 72-64/2
        = 72-32
        = 40
         */
        int b = 81/9-6*4;
        /*
        = 9-6*4
        = 9-24
        = -15
         */
        System.out.println(a);
        System.out.println(b);
        System.out.println("Made By Rexy");



    }
}
